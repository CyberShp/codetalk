# ---------------------------------------------------------------------------

MODULE_SUMMARY_PROMPT = """\
你是一名资深代码分析专家。请根据以下模块信息，生成一份简洁的模块摘要。

## 模块名称
{module_name}

## 模块文件列表
{file_list}

## 模块内调用关系
{call_relations}

## 模块 Wiki 文档
{wiki_content}

## 输出要求
请用中文输出，包含以下部分：
1. **模块职责**（一句话概括）
2. **核心组件**（列出 3-5 个关键文件/类/函数及其作用）
3. **对外接口**（该模块暴露给其他模块的 API/函数/类）
4. **内部依赖**（模块内部的关键调用链路）
5. **技术要点**（使用的设计模式、框架特性、特殊算法等）

请控制总字数在 200 字以内，语言简练、信息密度高。
"""


"""Chinese prompt templates for CodeTalk report generation.

Each template is a format-string that accepts structured context (code graph data,
wiki content, module info) and instructs the LLM to generate a specific report section.

Gray-box style principle (铲屎官定义):
  业务场景为主 → 代码定位为辅（模块/函数名，不暴露行号签名）→ 测试关注点结尾

Tool priority (from prompt.txt): GitNexus → CGC → 源码（最终事实依据）
Conflict rule: 若证据卡来自不同工具且结论冲突，以源码引用为准，标注"待源码验证"
"""

import re

_FORMATTING_RULES = """\
## CodeTalk formatting contract
- The AI writes narrative prose, concise bullets, and code excerpts only.
- Do not emit Markdown tables, Mermaid code fences, or SFMEA grids.
- CodeTalk renders tables, diagrams, and document layout deterministically
  after the AI response, using the evidence objects already owned by the
  analysis pipeline.
- Keep section headings clear and cite evidence where possible.
"""

_LAYOUT_DELEGATION_NOTICE = """\
## CodeTalk layout ownership
Write source-grounded analysis only. CodeTalk will render Markdown tables,
Mermaid diagrams, SFMEA grids, and other layout-heavy blocks. Do not output
Markdown table grids or Mermaid code fences.
"""


def _delegate_layout_to_codetalk_prompt(prompt: str) -> str:
    """Strip legacy layout examples so internal LLMs only write prose."""

    prompt = re.sub(
        r"```mermaid\b.*?```",
        "[CodeTalk will render the diagram block.]",
        prompt,
        flags=re.DOTALL,
    )
    kept: list[str] = []
    for line in prompt.splitlines():
        stripped = line.strip()
        if stripped.startswith("|") and stripped.endswith("|"):
            continue
        if "Mermaid" in stripped and "CodeTalk" not in stripped:
            continue
        if "表格" in stripped and "CodeTalk" not in stripped:
            continue
        kept.append(line)
    return _LAYOUT_DELEGATION_NOTICE + "\n" + "\n".join(kept).strip()

_EXECUTION_LIMITS = """\
## 执行限制（必须遵守）
1. 不要无差别全量阅读源码；优先使用 GitNexus / CGC 图谱结果，再定向阅读源码
2. 每条关键调用链最多展开 3 层；更深路径标注"需继续追踪"
3. 对不确定内容必须标注 `待验证`，不得省略，不得编造
4. 若 GitNexus 与 CGC 结论不一致，以源码为准
5. **若证据卡来自不同工具且结论冲突，以源码引用为准，并在对应结论后标注"待源码验证"**
6. 输出面向测试人员：业务场景为主，代码定位为辅（模块/函数名，不暴露行号）
7. SFMEA 和测试建议段落为纯黑盒，只描述可观测现象，不暴露内部实现细节
8. 证据不足时显式写"数据不足，无法分析"，不得用通用描述填充
9. 不追求 100% 分支覆盖，重点关注：业务流程 / 异常传播路径 / 可观测点 / 异常注入点
"""


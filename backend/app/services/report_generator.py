    @staticmethod
    def _truncate(text: str, max_chars: int) -> str:
        """Truncate text to stay within token budget.

        When tiktoken is available, *max_chars* is treated as a **token** limit
        and the text is truncated at a token boundary.  Otherwise we fall back
        to approximating tokens as ``len(text) // 4`` and cutting by character
        count.
        """
        if _tiktoken_enc is not None:
            tokens = _tiktoken_enc.encode(text)
            if len(tokens) <= max_chars:
                return text
            truncated_tokens = tokens[:max_chars]
            result = _tiktoken_enc.decode(truncated_tokens)
            logger.info(
                "Truncated context: %d -> %d tokens",
                len(tokens),
                len(truncated_tokens),
            )
            return result + "\n...（已截断）"

        # Fallback: approximate tokens as chars / 4
        approx_tokens = len(text) // 4
        if approx_tokens <= max_chars:
            return text
        char_limit = max_chars * 4
        result = text[:char_limit]
        logger.info(
            "Truncated context (char approx): ~%d -> ~%d tokens",
            approx_tokens,
            max_chars,
        )
        return result + "\n...（已截断）"

    @staticmethod
    def _read_source_for_key_files(
        repo_path: str,
        gitnexus_data: dict,
        max_total_bytes: int = MAX_SOURCE_BYTES,
    ) -> str:
        """Read actual source files for key files identified by GitNexus edge count."""
        if not repo_path:
            return "（无源码路径）"

        repo = Path(repo_path)
        if not repo.is_dir():
            return "（源码路径不存在）"

        nodes = gitnexus_data.get("nodes", [])
        relationships = gitnexus_data.get("relationships", [])

        edge_count: dict[str, int] = {}
        for edge in relationships:
            edge_count[edge["sourceId"]] = edge_count.get(edge["sourceId"], 0) + 1
            edge_count[edge["targetId"]] = edge_count.get(edge["targetId"], 0) + 1

        file_nodes = [n for n in nodes if n.get("label") in ("File", "Module")]
        ranked = sorted(
            file_nodes, key=lambda n: edge_count.get(n["id"], 0), reverse=True
        )

        collected: list[str] = []
        total = 0

        for node in ranked[:15]:
            props = node.get("properties", {})
            name = props.get("name", node["id"])

            if Path(name).suffix not in _SOURCE_EXTS:
                continue

            candidate = repo / name
            if not candidate.is_file():
                continue

            try:
                size = candidate.stat().st_size
                if total + size > max_total_bytes:
                    continue
                content = candidate.read_text(encoding="utf-8", errors="replace")
                rel = candidate.relative_to(repo)
                ext = candidate.suffix.lstrip(".")
                collected.append(f"### {rel}\n```{ext}\n{content}\n```")
                total += size
            except OSError:
                continue

        if not collected:
            return "（未能读取源代码文件）"

        logger.info("Injected %d source files (%d bytes) into report prompt", len(collected), total)
        return "## 源代码内容（以下为实际文件内容，请据此分析）\n\n" + "\n\n".join(collected)



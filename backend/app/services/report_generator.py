    @staticmethod
    def _source_windows(file_path: str, needles: tuple[str, ...], before: int = 400, after: int = 800) -> str:
        try:
            source_text = Path(file_path).read_text(encoding="utf-8", errors="replace")
        except OSError:
            return ""

        windows: list[str] = []
        seen: set[tuple[int, int]] = set()
        for needle in needles:
            idx = source_text.find(needle)
            if idx < 0:
                continue
            start = max(0, idx - before)
            end = min(len(source_text), idx + after)
            key = (start, end)
            if key in seen:
                continue
            seen.add(key)
            windows.append(source_text[start:end])
        return "\n".join(windows)


# Boilerplate openings the LLM sometimes prepends despite the prompt.
# Matched only at the very start of the body (optionally after a blank line).
_PREAMBLE_PATTERNS = (
    r"好的[，。:\s]*",
    r"下面是[^\n]*[:：\s]*",
    r"作为[^\n]{0,40}[:：\s]*",
    r"我将[^\n]*[:：。\s]*",
    r"I will[^\n]*[:：\s]*",
)
_PREAMBLE_RE = re.compile(
    r"^\s*(?:" + "|".join(_PREAMBLE_PATTERNS) + r")", re.UNICODE,
)


def strip_llm_preamble(text: str) -> str:
    """Remove leading LLM boilerplate so reports stay clean (P1).

    Strips a small, bounded number of opening 瀵掓殑 phrases (濂界殑銆佷綔涓衡€︺€佷笅闈㈡槸鈥?
    from the start of a section.  Conservative: only touches the first couple of
    lines and never removes real Markdown headings or content.
    """
    if not text:
        return text
    out = text.lstrip("\n")
    for _ in range(3):  # at most a few stacked openers
        new = _PREAMBLE_RE.sub("", out, count=1)
        if new == out:
            break
        # Drop whitespace and any leftover separator punctuation the opener
        # left behind (e.g. the "銆? after "浣滀负鈥︿笓瀹?).
        out = new.lstrip(" \t:：，。")
    return out.lstrip("\n")



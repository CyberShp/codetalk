"""Report generator -- takes module summaries + LLM client, generates each report file.

Each report is generated by:
1. Formatting the prompt template with context data
2. Calling the LLM
3. Saving the markdown output to data/outputs/{task_id}/XX-报告名.md

F-WORKSPACE-GITNEXUS-ANALYSIS-TASK-REDESIGN adds a plan-driven path that
generates each report section-by-section, retries empty/truncated output,
and writes a ``report_manifest.json`` so the workspace pipeline can mark
reports as ``completed``, ``partial`` or ``failed`` instead of silently
shipping blank files.
"""


def scrub_tool_orchestration_contradictions(content: str, common_context: dict) -> str:
    """Remove LLM claims that tool orchestration evidence is missing when it is fixed.

    Runtime coverage is injected deterministically above. If GitNexus and CGC
    both participated, an LLM sentence saying that no GitNexus/CGC orchestration
    evidence was provided is a report contradiction, not a useful caveat.
    """
    if not content:
        return content
    mode = common_context.get("pipeline_mode", "dual")
    if mode not in ("dual", "cgc_only"):
        return content
    info = common_context.get("index_coverage") or {}
    if not info.get("cgc_index_root"):
        return content
    cleaned = _TOOL_ORCHESTRATION_FALSE_MISSING_RE.sub(
        "> Tool orchestration evidence is summarized in section 00 above.", content
    )
    cleaned = _TOOL_ORCHESTRATION_VERIFY_RE.sub(
        "> Tool orchestration evidence is summarized in section 00 above.", cleaned
    )
    return cleaned



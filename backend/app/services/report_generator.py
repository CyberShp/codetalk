_TABLE_RE = re.compile(r"\|.+\|.+\|")
_MERMAID_RE = re.compile(r"```mermaid\b")
_MERMAID_REQUIRED_TYPES = frozenset({"module_map", "business_flow"})


def _validate_report_structure(content: str, report_type: str) -> str:
    """Check LLM output for required structural elements.

    Returns the content with a warning appended if structure is incomplete.
    Non-blocking: never raises.
    """
    missing: list[str] = []

    if not _TABLE_RE.search(content):
        missing.append("Markdown 表格")

    if report_type in _MERMAID_REQUIRED_TYPES and not _MERMAID_RE.search(content):
        missing.append("Mermaid 图")

    if not missing:
        return content

    logger.warning(
        "Report %s missing structural elements: %s",
        report_type,
        ", ".join(missing),
    )
    warning = (
        "\n\n> ⚠ 本报告格式未完全符合规范，"
        f"缺失: {', '.join(missing)}"
    )
    return content + warning


class ReportGenerator:
    """Generates analysis report files from module summaries via LLM."""

    def __init__(
        self,
        llm_client: BaseLLMClient,
        output_dir: Path,
        task_id: str,
    ) -> None:
        self._llm = llm_client
        self._output_dir = output_dir
        self._task_id = task_id
        self._generated_files: list[str] = []
        self._status_override: str | None = None

    @property
    def generated_files(self) -> list[str]:
        return list(self._generated_files)

    @property
    def status_override(self) -> str | None:
        """Non-None when report generation decided to alter the final task status."""
        return self._status_override

    # ------------------------------------------------------------------
    # F-WORKSPACE-GITNEXUS-ANALYSIS-TASK-REDESIGN
    # Plan-driven path: section-by-section LLM calls + programmatic
    # Markdown assembly + per-section retry on empty / truncated output.
    # ------------------------------------------------------------------

    async def generate_from_plan(
        self,
        *,
        plan: AnalysisPlan,
        scope_preview: ScopePreview | None,
        analysis_units: list[dict],
        evidence_cards: list,
        module_summaries: list[dict],
        gitnexus_data: dict,
        deepwiki_data: dict,
        requirements_doc: str | None = None,
        design_doc: str | None = None,
        on_report_done: Callable[[str, str, int], Awaitable[None]] | None = None,
        on_report_start: Callable[[str, int, int], Awaitable[None]] | None = None,
        on_report_failed: Callable[[str, str], Awaitable[None]] | None = None,
        sem: asyncio.Semaphore | None = None,
        data_quality: str = "good",
        repo_path: str = "",
    ) -> list[dict]:
        """Generate every enabled report in *plan* using small LLM calls.

        *sem* is the pipeline-owned global LLM semaphore.  All section-level
        LLM calls share this budget so total concurrent calls never exceed
        ``LLM_MAX_CONCURRENCY``.  If not provided a unit semaphore is used
        (concurrency=1, safe fallback).

        Returns a list of manifest entries (also persisted as
        ``report_manifest.json`` in the output dir).
        """
        enabled = [r for r in plan.reports if r.enabled]
        if not enabled:
            return []

        sem = sem if sem is not None else asyncio.Semaphore(1)
        total = len(enabled)

        # Context blob reused across reports (small — we cap each piece).
        common_context = {
            "analysis_objects": [
                {"id": o.id, "text": o.text, "kind": o.kind, "priority": o.priority}
                for o in plan.analysis_objects
            ],
            "scope_warnings": (scope_preview.warnings if scope_preview else []),
            "gitnexus_available": (
                scope_preview.gitnexus_available if scope_preview else True
            ),
            "data_quality": data_quality,
            "user_guidance": plan.user_guidance,
            "focus": plan.focus.model_dump(),
            "limits": plan.llm_limits.model_dump(),
            "has_requirements": bool(requirements_doc),
            "has_design": bool(design_doc),
            "requirements_doc_excerpt": (requirements_doc or "")[:3000],
            "design_doc_excerpt": (design_doc or "")[:3000],
            "module_summaries": module_summaries,
        }

        manifest_entries: list[dict] = []

        async def _run_one(idx: int, spec: ReportSpec) -> dict:
            if on_report_start is not None:
                await on_report_start(spec.template_id, idx + 1, total)
            entry = await self._generate_plan_report(
                spec=spec,
                plan=plan,
                common_context=common_context,
                analysis_units=analysis_units,
                evidence_cards=evidence_cards,
                sem=sem,
            )
            if entry["status"] == "completed":
                if on_report_done is not None:
                    await on_report_done(
                        spec.template_id,
                        entry["filename"],
                        entry["metadata"].get("char_count", 0),
                    )
            else:
                if on_report_failed is not None:
                    await on_report_failed(
                        spec.template_id, entry.get("error") or "(unknown)",
                    )
            return entry

        results = await asyncio.gather(
            *[_run_one(i, spec) for i, spec in enumerate(enabled)],
            return_exceptions=True,
        )
        for r in results:
            if isinstance(r, BaseException):
                logger.exception("Plan-driven report failed: %s", r)
                manifest_entries.append({
                    "report_type": "unknown",
                    "filename": "",
                    "status": "failed",
                    "error": str(r),
                    "metadata": {},
                })
            else:
                manifest_entries.append(r)

        any_completed = any(e["status"] == "completed" for e in manifest_entries)
        any_failed = any(e["status"] != "completed" for e in manifest_entries)
        if any_completed and any_failed:
            self._status_override = "completed_with_warnings"
        elif not any_completed:
            self._status_override = "failed"

        # Persist manifest for workspace_pipeline._harvest_reports.
        manifest = {
            "version": "workspace-report-manifest-v1",
            "task_id": self._task_id,
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "reports": manifest_entries,
        }
        manifest_path = self._output_dir / "report_manifest.json"
        try:
            await asyncio.to_thread(
                manifest_path.write_text,
                json.dumps(manifest, ensure_ascii=False, indent=2),
                "utf-8",
            )
        except Exception as exc:
            logger.warning("Failed to write report manifest: %s", exc)

        return manifest_entries

    async def _generate_plan_report(
        self,
        *,
        spec: ReportSpec,
        plan: AnalysisPlan,
        common_context: dict,
        analysis_units: list[dict],
        evidence_cards: list,
        sem: asyncio.Semaphore,
    ) -> dict:
        """Build one report file from small section calls, sections run in parallel."""
        sections_blueprint = _SECTION_BLUEPRINTS.get(spec.template_id)
        if sections_blueprint is None:
            sections_blueprint = _default_sections_for(spec)

        filename = _plan_report_filename(spec)
        section_outputs: list[tuple[str, str, str]] = []  # (heading, body, status)
        any_failure = False

        async def _do_section(section_idx: int, section: dict) -> tuple[str, str, str]:
            body, status = await self._render_section(
                spec=spec,
                section=section,
                plan=plan,
                common_context=common_context,
                analysis_units=analysis_units,
                evidence_cards=evidence_cards,
                section_idx=section_idx,
                sem=sem,
            )
            return (section["heading"], body, status)

        raw_results = await asyncio.gather(
            *[_do_section(i, s) for i, s in enumerate(sections_blueprint)],
            return_exceptions=True,
        )
        for r in raw_results:
            if isinstance(r, BaseException):
                logger.exception("Section generation failed: %s", r)
                section_outputs.append(("(section error)", str(r), "failed"))
                any_failure = True
            else:
                section_outputs.append(r)
                if r[2] != "completed":
                    any_failure = True

        # Assemble the file programmatically per §13.1.
        now = datetime.now(timezone.utc).isoformat()
        header = (
            f"---\n"
            f"report_type: {spec.template_id}\n"
            f"template_id: {spec.template_id}\n"
            f"task_id: {self._task_id}\n"
            f"generated_at: {now}\n"
            f"plan_version: {plan.version}\n"
            f"---\n\n"
        )
        body_parts: list[str] = [f"# {spec.title}\n"]
        body_parts.append("> 本报告由小段 LLM 调用组装而成。证据不足处会显式标记 `待验证`。\n")
        if not common_context.get("gitnexus_available", True):
            body_parts.append("> ⚠ GitNexus 图谱不可用，已退回到本地搜索结果。\n")
        for heading, body, status in section_outputs:
            body_parts.append(f"\n## {heading}\n")
            if status == "completed":
                body_parts.append(body.rstrip() + "\n")
            elif status == "partial":
                body_parts.append(body.rstrip() + "\n\n> ⚠ 该小节内容不足，已重试后仍未达标。\n")
            else:
                body_parts.append(
                    "> ❌ 本小节生成失败：" + (body or "LLM 未返回内容") + "\n"
                )

        full_content = header + "\n".join(body_parts)
        filepath = self._output_dir / filename
        try:
            await asyncio.to_thread(filepath.write_text, full_content, "utf-8")
            self._generated_files.append(filename)
        except Exception as exc:
            logger.exception("Failed to write report %s: %s", filename, exc)
            return {
                "report_type": spec.template_id,
                "template_id": spec.template_id,
                "filename": filename,
                "title": spec.title,
                "status": "failed",
                "error": f"write failed: {exc}",
                "metadata": {},
            }

        status = "failed" if not any(s == "completed" for _, _, s in section_outputs) else (
            "partial" if any_failure else "completed"
        )
        return {
            "report_type": spec.template_id,
            "template_id": spec.template_id,
            "filename": filename,
            "title": spec.title,
            "status": status,
            "error": None if status == "completed" else "部分小节生成不足或失败",
            "metadata": {
                "section_count": len(section_outputs),
                "completed_sections": sum(1 for _, _, s in section_outputs if s == "completed"),
                "char_count": sum(len(b) for _, b, _ in section_outputs),
            },
        }

    async def _render_section(
        self,
        *,
        spec: ReportSpec,
        section: dict,
        plan: AnalysisPlan,
        common_context: dict,
        analysis_units: list[dict],
        evidence_cards: list,
        section_idx: int,
        sem: asyncio.Semaphore,
    ) -> tuple[str, str]:
        """Call the LLM for a single section, retrying once on empty output.

        Returns ``(body, status)`` where status ∈ {completed, partial, failed}.
        """
        # Pick a bounded slice of evidence relevant to the section.  We
        # currently use the whole unit list rounded-robin; real signal
        # comes from the LLM prompt itself.
        relevant_cards = []
        card_cap = plan.llm_limits.max_cards_per_report_section
        unit_iters = [iter(u.get("cards", [])) for u in analysis_units]
        while len(relevant_cards) < card_cap and unit_iters:
            exhausted = []
            for i, it in enumerate(unit_iters):
                if len(relevant_cards) >= card_cap:
                    break
                card = next(it, None)
                if card is None:
                    exhausted.append(i)
                else:
                    relevant_cards.append(card)
            for i in reversed(exhausted):
                unit_iters.pop(i)

        evidence_md = "\n\n".join(c.to_markdown() for c in relevant_cards) or "（未找到证据卡）"
        unit_summary = "\n".join(
            f"- {u.get('title', '')}" for u in analysis_units
        ) or "（无分析单元）"
        max_chars = plan.llm_limits.max_output_chars_per_section
        focus_active = ", ".join(k for k, v in plan.focus.model_dump().items() if v) or "无"

        guidance = (plan.user_guidance or "")[:1500]

        prompt = (
            "你是资深代码分析与测试专家。请严格基于提供的证据卡撰写一份报告小节，"
            "禁止编造文件名、函数名或调用关系。证据不足处必须显式写"
            "“数据不足，请验证”，并标注 `待验证`。\n\n"
            f"## 报告：{spec.title}\n"
            f"## 当前小节：{section['heading']}\n"
            f"### 写作要点\n{section['instructions']}\n\n"
            f"### 输出约束\n- 字数控制在 {max_chars} 字以内\n"
            "- 优先使用 Markdown 表格/代码块/调用链\n"
            "- 不要重复报告标题或元信息\n"
            "- 重要结论必须附带证据来源（文件或符号）\n"
        )
        if focus_active:
            prompt += f"\n### 焦点方向\n{focus_active}\n"
        if guidance:
            prompt += f"\n### 用户附加说明（仅作风格补充）\n{guidance}\n"
        if section.get("requires_sfmea"):
            prompt += (
                "\n### 强制要求\n本小节必须包含 SFMEA 表格，列标题为："
                "功能/流程 | 故障模式 | 触发条件 | 异常注入点 | 异常传播路径 | "
                "影响 | 可观测现象 | 严重度 | 发生概率 | 检测难度 | 建议测试\n"
            )
        if section.get("requires_mermaid"):
            prompt += (
                "\n### 强制要求\n本小节必须包含至少一段 Mermaid 序列图或流程图，"
                "用 ```mermaid 代码块包裹。\n"
            )
        prompt += (
            f"\n## 分析单元概览\n{unit_summary}\n"
            f"\n## 证据卡（最多 {card_cap} 条）\n{evidence_md}\n"
        )

        messages = [{"role": "user", "content": prompt}]
        budget_tokens = min(
            settings.llm_max_output_tokens, max(800, max_chars * 2),
        )
        min_chars = max(60, section.get("min_chars", 80))

        has_real_streaming = (
            type(self._llm).stream_complete is not BaseLLMClient.stream_complete
        )
        retries = max(0, plan.llm_limits.retry_empty_output) + 1
        last_error: str | None = None
        body = ""
        for attempt in range(retries):
            try:
                async with sem:
                    if has_real_streaming:
                        body = await self._llm.stream_complete_collected(
                            messages=messages,
                            max_tokens=budget_tokens,
                            temperature=0.3,
                            min_chars=min_chars,
                            retry_on_empty=False,
                            max_retries=0,
                        )
                    else:
                        resp = await self._llm.complete(
                            messages=messages, max_tokens=budget_tokens, temperature=0.3,
                        )
                        body = resp.content
                if _section_is_invalid(body, min_chars=min_chars, section=section):
                    raise LLMEmptyOutputError("section content failed structural validation")
                return body.strip(), "completed"
            except LLMEmptyOutputError as exc:
                last_error = str(exc)
                logger.warning(
                    "Section '%s' empty/invalid on attempt %d/%d: %s",
                    section["heading"], attempt + 1, retries, exc,
                )
            except Exception as exc:
                last_error = str(exc)
                logger.exception(
                    "Section '%s' LLM call failed on attempt %d/%d", section["heading"], attempt + 1, retries,
                )
                break

        if body and len(body.strip()) >= 20:
            return body.strip(), "partial"
        return (last_error or "LLM 返回为空"), "failed"


    async def generate_all(
        self,
        module_summaries: list[dict],
        gitnexus_data: dict,
        deepwiki_data: dict,
        requirements_doc: str | None = None,
        design_doc: str | None = None,
        analysis_focus: str = "",
        prompt_content: str = "",
        on_report_done: Callable[[str, str, int], Awaitable[None]] | None = None,
        on_report_start: Callable[[str, int, int], Awaitable[None]] | None = None,
        on_report_failed: Callable[[str, str], Awaitable[None]] | None = None,
        sem: asyncio.Semaphore | None = None,
        data_quality: str = "good",
        use_streaming: bool = False,
        repo_path: str = "",
    ) -> list[str]:
        """Generate all applicable reports and return list of filenames."""
        if not module_summaries:
            logger.warning(
                "No module summaries available for task %s -- "
                "skipping report generation (completed_with_warnings)",
                self._task_id,
                extra={"task_id": self._task_id},
            )
            self._status_override = "completed_with_warnings"
            return self._generated_files

        summaries_text = self._format_summaries(module_summaries)

        content_prefix = _DATA_QUALITY_PREFIX.get(data_quality, "")

        if prompt_content:
            content_prefix += (
                f"## 用户自定义分析提示词\n{prompt_content[:4000]}\n\n"
                "请参考以上分析方法论和要求展开分析。\n\n"
            )
        elif analysis_focus:
            content_prefix += (
                f"## 用户分析目标\n{analysis_focus}\n\n"
                "请围绕此目标重点展开分析。\n\n"
            )

        source_code = await asyncio.to_thread(
            self._read_source_for_key_files, repo_path, gitnexus_data,
        ) if repo_path else "（无源码路径）"

        # Build all applicable report coroutines then run them with concurrency control
        report_tasks: list[tuple[str, Awaitable]] = [
            (
                "module_map",
                self._generate_report(
                    report_type="module_map",
                    prompt=content_prefix + MODULE_MAP_PROMPT.format(
                        project_overview=self._build_project_overview(gitnexus_data, deepwiki_data),
                        module_summaries=summaries_text,
                        inter_module_deps=self._build_inter_module_deps(gitnexus_data),
                    ),
                    on_report_done=on_report_done,

                    use_streaming=use_streaming,
                ),
            ),
            (
                "business_flow",
                self._generate_report(
                    report_type="business_flow",
                    prompt=content_prefix + BUSINESS_FLOW_PROMPT.format(
                        module_summaries=summaries_text,
                        process_data=self._build_process_data(gitnexus_data, deepwiki_data),
                        cross_module_calls=self._build_cross_module_calls(gitnexus_data, deepwiki_data),
                        source_code=source_code,
                    ),
                    on_report_done=on_report_done,

                    use_streaming=use_streaming,
                ),
            ),
            (
                "source_reading",
                self._generate_report(
                    report_type="source_reading",
                    prompt=content_prefix + SOURCE_READING_PROMPT.format(
                        module_summaries=summaries_text,
                        key_files=self._build_key_files(gitnexus_data, deepwiki_data),
                        call_graph=self._build_call_graph(gitnexus_data, deepwiki_data),
                        source_code=source_code,
                    ),
                    on_report_done=on_report_done,

                    use_streaming=use_streaming,
                ),
            ),
            (
                "test_design",
                self._generate_report(
                    report_type="test_design",
                    prompt=content_prefix + TEST_DESIGN_PROMPT.format(
                        module_summaries=summaries_text,
                        business_flows=self._build_process_data(gitnexus_data, deepwiki_data),
                        api_interfaces=self._build_api_interfaces(gitnexus_data, deepwiki_data),
                    ),
                    on_report_done=on_report_done,

                    use_streaming=use_streaming,
                ),
            ),
        ]

        # Optional reports: only if requirements/design docs provided
        if requirements_doc:
            report_tasks.append((
                "requirements",
                self._generate_report(
                    report_type="requirements",
                    prompt=content_prefix + REQUIREMENTS_PROMPT.format(
                        requirements_doc=self._truncate(requirements_doc, 5000),
                        design_doc=self._truncate(design_doc or "（未提供设计文档）", 5000),
                        module_summaries=summaries_text,
                    ),
                    on_report_done=on_report_done,

                    use_streaming=use_streaming,
                ),
            ))

        if requirements_doc and design_doc:
            report_tasks.append((
                "traceability",
                self._generate_report(
                    report_type="traceability",
                    prompt=content_prefix + TRACEABILITY_PROMPT.format(
                        requirements_items=self._truncate(requirements_doc, 3000),
                        design_modules=self._truncate(design_doc, 3000),
                        module_summaries=summaries_text,
                        call_relations=self._build_call_graph(gitnexus_data, deepwiki_data),
                    ),
                    on_report_done=on_report_done,

                    use_streaming=use_streaming,
                ),
            ))

        sem = sem if sem is not None else asyncio.Semaphore(1)
        total = len(report_tasks)

        async def _guarded(idx: int, rtype: str, coro: Awaitable) -> None:
            async with sem:
                if on_report_start is not None:
                    await on_report_start(rtype, idx + 1, total)
                return await coro

        results = await asyncio.gather(
            *[_guarded(i, rtype, coro) for i, (rtype, coro) in enumerate(report_tasks)],
            return_exceptions=True,
        )

        for i, r in enumerate(results):
            if isinstance(r, BaseException):
                report_type = report_tasks[i][0]
                error_message = str(r)
                logger.error(
                    "Report generation failed in parallel: %s",
                    r,
                    extra={"task_id": self._task_id},
                )
                self._status_override = "completed_with_warnings"
                if on_report_failed is not None:
                    await on_report_failed(report_type, error_message)

        return self._generated_files

    async def _generate_report(
        self,
        report_type: str,
        prompt: str,
        on_report_done: Callable[[str, str, int], Awaitable[None]] | None = None,
        use_streaming: bool = False,
    ) -> None:
        """Generate a single report and save to disk."""
        filename = REPORT_FILE_MAP.get(report_type)
        if not filename:
            logger.warning("Unknown report type: %s", report_type)
            return

        try:
            messages = [{"role": "user", "content": prompt}]

            # Use streaming when requested AND the client has a real stream_complete
            # (not the base class fallback that just wraps complete()).
            has_real_streaming = (
                type(self._llm).stream_complete is not BaseLLMClient.stream_complete
            )

            if use_streaming and has_real_streaming:
                full_content = await self._llm.stream_complete_collected(
                    messages=messages,
                    max_tokens=min(MAX_OUTPUT_TOKENS, settings.llm_max_output_tokens),
                    temperature=0.3,
                )

                total_tokens = BaseLLMClient.estimate_tokens(full_content)
                # Use a placeholder model name since streaming doesn't return model info
                model_name = type(self._llm).__name__

                now = datetime.now(timezone.utc).isoformat()
                header = (
                    f"---\n"
                    f"report_type: {report_type}\n"
                    f"task_id: {self._task_id}\n"
                    f"generated_at: {now}\n"
                    f"model: {model_name}\n"
                    f"tokens: {total_tokens}\n"
                    f"---\n\n"
                )

                full_content = _validate_report_structure(full_content, report_type)

                filepath = self._output_dir / filename
                content = header + full_content
                await asyncio.to_thread(filepath.write_text, content, "utf-8")
                self._generated_files.append(filename)

                if on_report_done is not None:
                    await on_report_done(report_type, filename, total_tokens)

                logger.info(
                    "Report %s generated (streaming): %s (~%d tokens)",
                    report_type,
                    filename,
                    total_tokens,
                    extra={"task_id": self._task_id},
                )
            else:
                response: LLMResponse = await self._llm.complete(
                    messages=messages,
                    max_tokens=min(MAX_OUTPUT_TOKENS, settings.llm_max_output_tokens),
                    temperature=0.3,
                )

                # Prepend metadata header
                now = datetime.now(timezone.utc).isoformat()
                header = (
                    f"---\n"
                    f"report_type: {report_type}\n"
                    f"task_id: {self._task_id}\n"
                    f"generated_at: {now}\n"
                    f"model: {response.model}\n"
                    f"tokens: {response.usage.get('total_tokens', 0)}\n"
                    f"---\n\n"
                )

                validated_content = _validate_report_structure(response.content, report_type)

                filepath = self._output_dir / filename
                content = header + validated_content
                await asyncio.to_thread(filepath.write_text, content, "utf-8")
                self._generated_files.append(filename)

                total_tokens = response.usage.get("total_tokens", 0)
                if on_report_done is not None:
                    await on_report_done(report_type, filename, total_tokens)

                logger.info(
                    "Report %s generated: %s (%d tokens)",
                    report_type,
                    filename,
                    response.usage.get("total_tokens", 0),
                    extra={"task_id": self._task_id},
                )

        except Exception as exc:
            logger.exception("Report generation failed for %s: %s", report_type, exc, extra={"task_id": self._task_id})
            raise


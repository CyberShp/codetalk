def _ctd_cell(value: object) -> str:
    """Sanitize a value for a Markdown table cell."""
    text = " ".join(str(value if value is not None else "").split())
    return text.replace("|", "\\|") or "-"


def _ctd_case_type_label(case_type: object) -> str:
    mapping = {
        "black_box_ready": "black_box_ready",
        "black_box_hypothesis": "black_box_hypothesis",
        "gray_box_required": "gray_box_required",
    }
    return mapping.get(str(case_type or ""), str(case_type or "unknown"))


def _ctd_entry_discovery_summary(gap: dict) -> str:
    discovery = gap.get("entry_discovery") or {}
    candidates = discovery.get("candidate_external_entries") or []
    if candidates:
        parts: list[str] = []
        for item in candidates[:3]:
            metadata: list[str] = []
            provider = item.get("provider") or item.get("tool")
            verification = item.get("source_verification")
            turn_id = item.get("turn_id")
            if provider:
                metadata.append(f"provider={_ctd_cell(provider)}")
            if verification:
                metadata.append(f"verification={_ctd_cell(verification)}")
            if turn_id:
                metadata.append(f"turn={_ctd_cell(turn_id)}")
            suffix = f" ({'; '.join(metadata)})" if metadata else ""
            parts.append(
                "[{kind}] {label}{suffix}".format(
                    kind=item.get("entry_type") or item.get("entry_kind") or "external",
                    label=item.get("entry_label") or item.get("entry_symbol") or "外部入口候选",
                    suffix=suffix,
                )
            )
        return "；".join(parts)
    status = discovery.get("entry_trace_status") or gap.get("entry_trace_status")
    status_label = {
        "entry_found": "已确认外部入口",
        "source_read_ok_entry_not_found": "源码已读，入口仍需确认",
        "source_not_found": "未读到源码窗口",
        "workspace_not_bound": "未绑定工作区",
        "trace_skipped_by_cap": "超过追踪上限",
        "tool_unavailable": "工具不可用",
    }.get(str(status or ""), str(status or "入口发现状态未知"))
    return status_label


def _ctd_render_test_case_drafts(lines: list[str], gap: dict) -> None:
    drafts = gap.get("test_case_drafts") or []
    if not drafts:
        return
    lines.append("- 测试执行区：")
    for draft in drafts[:3]:
        execution = draft.get("test_execution") or {}
        lines.append(f"  - 类型：`{_ctd_case_type_label(draft.get('case_type'))}`")
        lines.append(f"    - 标题：{_ctd_cell(execution.get('title'))}")
        lines.append(f"    - 外部触发：{_ctd_cell(execution.get('external_trigger'))}")
        lines.append(f"    - 前置条件：{_ctd_cell(execution.get('preconditions'))}")
        lines.append(f"    - 测试输入：{_ctd_cell(execution.get('inputs'))}")
        steps = execution.get("steps") or []
        if steps:
            lines.append("    - 执行步骤：" + " -> ".join(_ctd_cell(step) for step in steps[:6]))
        lines.append(f"    - 预期结果：{_ctd_cell(execution.get('expected'))}")
        signals = execution.get("observable_signals") or []
        if signals:
            lines.append("    - 可观测信号：" + "; ".join(_ctd_cell(sig) for sig in signals[:8]))



def scrub_bdev_source_false_gaps(content: str) -> str:
    """Remove bdev source-gap claims once exact bdev source facts are present."""
    if not content or "bdev" not in content:
        return content
    has_guard = (
        "Pinned exact source facts" in content
        or "bdev->fn_table->submit_request(ioch, bdev_io)" in content
        or "bdev_queue_nomem_io_tail" in content
        or "TRACE_BDEV_IO_START" in content
    )
    if not has_guard:
        return content

    kept: list[str] = []
    inserted_guard = False
    lines = content.splitlines()
    for idx, line in enumerate(lines):
        local_window = "\n".join(lines[idx : idx + 5])
        has_guarded_term = any(term in line for term in _BDEV_SOURCE_GUARD_TERMS)
        has_local_guard = (
            has_guarded_term
            or "Source fact guard: bdev submit/read/write facts are backed" in local_window
            or (
                any(marker in line for marker in _BDEV_FALSE_GAP_MARKERS)
                and any(term in local_window for term in _BDEV_SOURCE_GUARD_TERMS)
            )
        )
        if (
            has_local_guard
            and any(marker in line for marker in _BDEV_FALSE_GAP_MARKERS)
        ):
            if not inserted_guard:
                kept.append(
                    "> Source fact guard: bdev submit/read/write facts are backed by "
                    "`lib/bdev/bdev.c` source evidence and must not be reported as missing."
                )
                inserted_guard = True
            continue
        kept.append(line)
    return "\n".join(kept)


def strip_duplicate_heading(body: str, heading: str) -> str:
    """Drop a leading Markdown heading that just repeats the section heading.

    The assembler already prints ``## {heading}`` before each section body, so
    when the LLM also opens with the same heading the report shows it twice
    (Round 3: repeated headings in 13-/15- reports).
    """
    if not body or not heading:
        return body
    lines = body.lstrip("\n").splitlines()
    if not lines:
        return body
    first = lines[0].strip()
    m = re.match(r"^#{1,6}\s+(.*\S)\s*$", first)
    if m:
        norm_h = re.sub(r"\s+", "", heading)
        norm_first = re.sub(r"\s+", "", m.group(1))
        # exact or one-contains-the-other (handles numbered prefixes)
        if norm_first == norm_h or norm_h in norm_first or norm_first in norm_h:
            return "\n".join(lines[1:]).lstrip("\n")
    return body


def looks_truncated(text: str) -> bool:
    """Heuristic detector for an LLM section that was cut off mid-output (P1).

    Used so a truncated section is never marked ``completed``.  Flags: dangling
    code fence, trailing ellipsis, an unterminated final table row, or a body
    ending on a bare list/enumeration marker (e.g. the ``| **2.`` tail the
    handoff observed).
    """
    if not text:
        return False
    t = text.rstrip()
    if not t:
        return False
    if t.count("```") % 2 != 0:
        return True
    if t.endswith("...") or t.endswith("…"):
        return True
    last = t.splitlines()[-1].strip()
    # Unterminated table row: starts like a table cell but has no closing pipe.
    if last.startswith("|") and not last.endswith("|"):
        return True
    # Ends on a dangling enumerator / heading with no content after it.
    if re.search(r"(?:\*\*)?\d+[\.\s]*(?:\*\*)?$", last):
        return True
    if re.search(r"[,，:：(\-]$", last) and len(last) <= 40:
        return True
    return False


def _section_is_invalid(body: str, *, min_chars: int, section: dict) -> bool:
    """Apply the 搂13.4 validity checks to a single section body."""
    if not body or len(body.strip()) < min_chars:
        return True
    text = body.strip()
    # Reject obvious truncation: dangling code fence / unmatched table row /
    # trailing ellipsis / half-finished enumerator.
    if looks_truncated(text):
        return True
    return False

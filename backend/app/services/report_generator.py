    @staticmethod
    def _format_pinned_source_facts(cards: list) -> str:
        lines: list[str] = []
        seen: set[str] = set()
        for card in cards:
            if ReportGenerator._is_exact_source_card(card):
                facts = ReportGenerator._summarize_source_facts(card)
                bdev_facts = ReportGenerator._summarize_bdev_source_facts(card)
                if bdev_facts:
                    facts = f"{facts}；{bdev_facts}" if facts else bdev_facts
                thread_facts = ReportGenerator._summarize_thread_source_facts(card)
                if thread_facts:
                    facts = f"{facts}；{thread_facts}" if facts else thread_facts
                if not facts or facts in seen:
                    continue
                seen.add(facts)
                symbol = getattr(card, "symbol", None) or getattr(card, "title", "source")
                lines.append(f"> - `{symbol}`: {facts}")
        if not lines:
            return ""
        return "> Pinned exact source facts (deterministic, do not omit):\n" + "\n".join(lines)


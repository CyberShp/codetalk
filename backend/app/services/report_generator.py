    @staticmethod
    def _format_requested_identifier_gaps(guidance: str, evidence_text: str) -> str:
        if not guidance:
            return ""
        requested = []
        for term in re.findall(r"`([^`]{3,80})`", guidance):
            term = term.strip()
            if term not in requested:
                requested.append(term)
        priority_terms = ("spdk_log_set_flag(optarg)", "unknown flag", "reset_log_deprecated")
        for term in priority_terms:
            if term in guidance and term not in requested:
                requested.append(term)
        gaps = [
            term for term in requested
            if term not in evidence_text and term not in {"NULL", "true", "false"}
        ]
        gaps.sort(key=lambda term: (0 if term in priority_terms else 1, requested.index(term)))
        if not gaps:
            return ""
        lines = [
            f"> - `{term}`: requested in user guidance, but no selected source evidence was found; mark 待验证 instead of inventing it."
            for term in gaps[:12]
        ]
        return "> Requested identifiers without source evidence:\n" + "\n".join(lines)

